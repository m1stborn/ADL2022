from utils import (
    PreprocessTitleGenTrain,
    postprocess_text,
)

logger = get_logger(__name__)
logging.basicConfig(
    format="%(asctime)s - %(levelname)s - %(name)s - %(message)s",
    datefmt="%m/%d/%Y %H:%M:%S",
    level=logging.INFO,
)
UID = str(uuid.uuid1())

